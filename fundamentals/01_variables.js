// Declaración de una constante
const gravedad = 9.8;
console.log("El valor de la gravedad es:", gravedad);

// Intentar reasignar una constante (esto dará error)
// gravedad = 10; // Esto causará un error: Assignment to constant variable.

// Declaración de una variable con 'let'
let nombreUsuario = "Ana";
console.log("Nombre de usuario inicial:", nombreUsuario);

// Reasignación de la variable 'nombreUsuario'
nombreUsuario = "Carlos";
console.log("Nombre de usuario actualizado:", nombreUsuario);

// Declaración de una variable con 'var'
var edad = 25;
console.log("Edad inicial:", edad);

// Reasignación de la variable 'edad'
edad = 30;
console.log("Edad actualizada:", edad);