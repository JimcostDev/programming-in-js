// Suma
console.log(10 + 10); // Salida: 20

// Resta
console.log(10 - 5); // Salida: 5

// Multiplicación
console.log(10 * 2); // Salida: 20

// División
console.log(10 / 2); // Salida: 5

// Módulo (resto de la división)
console.log(10 % 2); // Salida: 0
console.log(10 % 3); // Salida: 1

// Exponenciación
console.log(2 ** 3); // Salida: 8

// Jerarquía de operadores
console.log(2 ** (3 + 3) - 7 / 1); // Salida: 57

// Concatenación de cadenas
console.log('Hola' + ' mundo'); // Salida: 'Hola mundo'

// Repetición de cadenas
console.log('Hola'.repeat(20)); // Salida: 'HolaHolaHola...Hola' (20 veces)
