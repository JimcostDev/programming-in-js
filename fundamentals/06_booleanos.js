// Declaración de una variable booleana
let esSoltero = true;
console.log(typeof esSoltero); // Salida: 'boolean'
console.log(esSoltero); // Salida: true

// Cambio del valor de la variable booleana
esSoltero = false;
console.log(esSoltero); // Salida: false

// Operador lógico NOT
console.log(!true); // Salida: false
console.log(!false); // Salida: true

// Inversión del valor de la variable booleana
esSoltero = !esSoltero;
console.log(esSoltero); // Salida: true
