// Las funciones también pueden ser anónimas, es decir, no tienen un nombre. 
// Se pueden asignar a variables y se usan comúnmente en eventos 
// o como parámetros de otras funciones.

let saludo = function(nombre) {
    return "¡Hola, " + nombre + "!";
};
  
console.log(saludo("Ana"));  // Salida: ¡Hola, Ana!
  